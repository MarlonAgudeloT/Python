""" Operadores logicos """

# Y - and

variablFalsa = False
variableVerdadera = True
print("Usando and")
print("Opcion 1, dos verdaderos", variableVerdadera and variableVerdadera)
print("Opcion 2, un verdadero y un falso", variableVerdadera and variablFalsa)
print("Opcion , un false y un falso", variablFalsa and variablFalsa)

# O - or
print("Usando or")
print("Opcion 1, dos verdaderos", variableVerdadera or variableVerdadera)
print("Opcion 2, un verdadero o un falso", variableVerdadera or variablFalsa)
print("Opcion , un false o un falso", variablFalsa or variablFalsa)

#No - not
print(not(variableVerdadera))
print(not(variablFalsa))

#Igual
print("Usando ==")
print("La potencia de 5 elevada a la 2 es igual a 20", 5**2==20)
print("La potencia de 5 elevada a la 2 es igual a 20", 5**2==25)


